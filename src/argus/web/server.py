# ============================================================
# Security configuration
# ============================================================

# Allow scanning private/loopback ranges. Default OFF — operators must
# explicitly opt in for local benchmark testing. The benchmark runner sets
# this to "1" before starting the server.
ALLOW_PRIVATE_RANGES = os.environ.get("ARGUS_WEB_ALLOW_PRIVATE", "").lower() in ("1", "true", "yes")

# Auth token. Sourced from env or auto-generated.
_TOKEN_FROM_ENV = os.environ.get("ARGUS_WEB_TOKEN", "").strip()
WEB_TOKEN: str = _TOKEN_FROM_ENV or secrets.token_urlsafe(32)
_TOKEN_AUTOGENERATED = not bool(_TOKEN_FROM_ENV)

# Bounded state limits — protects against memory exhaustion from a malicious
# target that emits floods of findings.
MAX_FINDINGS_IN_MEMORY = 10_000
MAX_SIGNALS_IN_MEMORY = 50_000
MAX_RECENT_FINDINGS_RETURNED = 50

# Allowed URL schemes
ALLOWED_URL_SCHEMES = frozenset({"http", "https"})

# Blocked SSRF host patterns when ALLOW_PRIVATE_RANGES is False
SSRF_BLOCKED_EXACT_HOSTS = frozenset(
    {
        "localhost",
        "metadata.google.internal",
        "metadata",
    }
)


_bearer_scheme = HTTPBearer(auto_error=False)


def _validate_url_for_scan(url: str) -> None:
    """Validate a target URL for use in a scan request.

    Raises ValueError if the URL is malformed, uses a forbidden scheme,
    or points to a private/internal address (unless ALLOW_PRIVATE_RANGES).
    """
    if not url or not isinstance(url, str):
        raise ValueError("URL must be a non-empty string")

    parsed = urlparse(url)
    if parsed.scheme not in ALLOWED_URL_SCHEMES:
        raise ValueError(f"URL scheme '{parsed.scheme}' not allowed (use http:// or https://)")
    if not parsed.hostname:
        raise ValueError("URL must have a valid hostname")

    if ALLOW_PRIVATE_RANGES:
        return

    host = parsed.hostname.lower()
    if host in SSRF_BLOCKED_EXACT_HOSTS:
        raise ValueError(f"URL host '{host}' is blocked (private/metadata range)")

    # Check literal IPs first
    try:
        ip = ipaddress.ip_address(host)
        _check_ip_blocked(ip)
        return
    except ValueError as exc:
        if "points to" in str(exc):
            raise
        # Not a literal IP — fall through to DNS resolution

    # Resolve DNS and re-check every returned address. This closes the
    # DNS-rebinding bypass where an attacker registers `evil.example.com`
    # with an A record of 127.0.0.1 / 169.254.169.254 etc.
    # Note: this is best-effort. The real MCP/HTTP client should still
    # validate the resolved address at request time, since DNS responses
    # can change between this check and the actual connect.
    import socket as _socket

    try:
        infos = _socket.getaddrinfo(host, None)
    except OSError as exc:
        raise ValueError(f"URL host '{host}' could not be resolved: {type(exc).__name__}") from None

    for info in infos:
        addr = info[4][0]
        try:
            resolved_ip = ipaddress.ip_address(addr)
        except ValueError:
            continue
        _check_ip_blocked(resolved_ip)


def _check_ip_blocked(ip: ipaddress.IPv4Address | ipaddress.IPv6Address) -> None:
    """Raise ValueError if an IP address is in a blocked range."""
    if ip.is_loopback or ip.is_link_local or ip.is_private or ip.is_reserved or ip.is_multicast:
        raise ValueError(f"URL points to blocked IP range: {ip}")
    if str(ip) in ("169.254.169.254", "169.254.170.2"):
        raise ValueError(f"URL points to cloud metadata endpoint: {ip}")


class ScanRequest(BaseModel):
    """Request body for starting a scan."""

    target_name: str = "Untitled Target"
    mcp_urls: list[str] = []
    agent_endpoint: str | None = None
    timeout: float = 300.0
    demo_pace_seconds: float = 0.4

    @field_validator("mcp_urls")
    @classmethod
    def _validate_mcp_urls(cls, v: list[str]) -> list[str]:
        if len(v) > 50:
            raise ValueError("Too many MCP URLs (max 50)")
        for url in v:
            try:
                _validate_url_for_scan(url)
            except ValueError as exc:
                raise ValueError(f"Invalid MCP URL '{url}': {exc}") from exc
        return v

    @field_validator("agent_endpoint")
    @classmethod
    def _validate_agent_endpoint(cls, v: str | None) -> str | None:
        if v is None:
            return v
        try:
            _validate_url_for_scan(v)
        except ValueError as exc:
            raise ValueError(f"Invalid agent endpoint '{v}': {exc}") from exc
        return v

    @field_validator("timeout")
    @classmethod
    def _validate_timeout(cls, v: float) -> float:
        if v < 1.0 or v > 1800.0:
            raise ValueError("timeout must be between 1 and 1800 seconds")
        return v

    @field_validator("demo_pace_seconds")
    @classmethod
    def _validate_demo_pace(cls, v: float) -> float:
        if v < 0.0 or v > 10.0:
            raise ValueError("demo_pace_seconds must be between 0 and 10")
        return v

    @field_validator("target_name")
    @classmethod
    def _validate_target_name(cls, v: str) -> str:
        if len(v) > 200:
            raise ValueError("target_name too long (max 200 chars)")
        return v


