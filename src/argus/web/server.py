"""ARGUS Web Server.

FastAPI backend that runs scans and streams live events to the browser
via Server-Sent Events. The frontend is a single-page HTML app served
from the static/ directory.

Endpoints:
- GET /                 → dashboard HTML
- GET /api/status       → current scan status snapshot
- POST /api/scan/start  → kick off a new scan
- POST /api/scan/stop   → cancel running scan
- GET /api/events       → SSE stream of all scan events (signal bus + findings)
- GET /api/findings     → all findings from the most recent scan
- GET /api/health       → service liveness

Security:
- API token auth (Bearer) required on all /api/* routes except /api/health.
  Token is auto-generated on startup and printed to the console for the
  operator to use, OR sourced from ARGUS_WEB_TOKEN env var.
- ScanRequest URLs are validated and SSRF-blocked by default — private
  ranges (loopback, link-local, RFC1918) are rejected unless
  ARGUS_WEB_ALLOW_PRIVATE=1 is set (used by the local benchmark).
- CORS is locked down to same-origin by default.
- ScanState is bounded — findings/signals capped to prevent unbounded growth.
- Token comparison uses constant-time compare.
"""

